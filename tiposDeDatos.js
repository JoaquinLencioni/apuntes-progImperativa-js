// Dato del tipo Number

let edad = 22;
console.log(edad);

let precio = 150.6;
console.log(precio);

// Datos del tipo String

let nombre = "Joaquin";
console.log(nombre);

let apellido = 'Lencioni';
console.log(apellido);

// Dato del tipo booleano 

let verdadero = true;
console.log(verdadero);

let falso = false;
console.log(falso);

// Dato del tipo Not a Number

let notANumber = NaN;
console.log(notANumber);

// Dato del tipo Null

let nulo = null;
console.log(nulo);

// Dato del tipo Undefinded

let sinValor;
console.log(sinValor);

// Dato del tipo Objeto Literal

let persona = {
    nombre: 'Joaquin',
    edad: '22'
}
console.log(persona);

// Datos del tipo Array

let paises = [ 'Argentina', 'Estados Unidos', 'Francia' ];
console.log(paises);